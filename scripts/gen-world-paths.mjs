// Bakes world map SVG paths + marker positions at dev time → src/worldPaths.js
// Run: node scripts/gen-world-paths.mjs
import { writeFileSync } from "fs";
import { createRequire } from "module";
import { geoNaturalEarth1, geoPath } from "d3-geo";
import { feature, mesh } from "topojson-client";

const require = createRequire(import.meta.url);
const usAtlas  = require("us-atlas/states-10m.json");

const W = 800;
const H = 460;

// Continental US view
const proj = geoNaturalEarth1()
  .scale(750)
  .center([-96, 37])
  .translate([W / 2, H / 2]);

const pathGen = geoPath().projection(proj);

// ── state fills (individual paths for per-state fill) ────────────────────────
const statePaths = feature(usAtlas, usAtlas.objects.states).features
  .map((f) => pathGen(f))
  .filter(Boolean);

// ── internal state borders (single merged path, no outer coast repeated) ─────
const stateMesh = pathGen(
  mesh(usAtlas, usAtlas.objects.states, (a, b) => a !== b)
);

// ── outer nation border ───────────────────────────────────────────────────────
const nationPath = pathGen(
  mesh(usAtlas, usAtlas.objects.states, (a, b) => a === b)
);

// ── markers ─────────────────────────────────────────────────────────────────
const MARKERS_SRC = [
  { id: "nepal",       coords: [ 85.324,  27.717], label: "Kathmandu",         sub: "Nepal" },
  { id: "gatech",      coords: [-84.396,  33.776], label: "Georgia Tech",       sub: "Atlanta, GA",      ldx:  8, ldy: -2  },
  { id: "iowa",        coords: [-91.802,  42.842], label: "Upper Iowa Univ.",   sub: "Fayette, Iowa",    ldx:  8, ldy: -2  },
  { id: "santarosa",   coords: [-122.714, 38.440], label: "Santa Rosa",         sub: "CA",               ldx: -8, ldy: -12 },
  { id: "rohnertpark", coords: [-122.701, 38.340], label: "Sonoma State",       sub: "Rohnert Park, CA", ldx:  8, ldy: -6  },
  { id: "sf",          coords: [-122.419, 37.774], label: "San Francisco",      sub: "CA",               ldx:  8, ldy:  4  },
  { id: "emeryville",  coords: [-122.291, 37.831], label: "Emeryville",         sub: "CA",               ldx:  8, ldy: -6  },
  { id: "sanmateo",    coords: [-122.325, 37.563], label: "San Mateo",          sub: "CA",               ldx: -8, ldy:  12 },
];

const markers = MARKERS_SRC.map(({ coords, ...rest }) => {
  const [px, py] = proj(coords);
  const offMap = px < 0 || px > W || py < 0 || py > H;
  return { ...rest, px: +px.toFixed(2), py: +py.toFixed(2), offMap };
});

const out = `// AUTO-GENERATED by scripts/gen-world-paths.mjs — do not edit manually
export const W = ${W};
export const H = ${H};
export const STATE_PATHS = ${JSON.stringify(statePaths)};
export const STATE_MESH = ${JSON.stringify(stateMesh)};
export const NATION_PATH = ${JSON.stringify(nationPath)};
export const MARKERS = ${JSON.stringify(markers)};
`;

writeFileSync(new URL("../src/worldPaths.js", import.meta.url), out, "utf8");
console.log(`wrote ${statePaths.length} state paths, state mesh, nation outline, ${markers.length} markers`);
